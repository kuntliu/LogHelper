package com.kuntliu.loghelper;

public class PrivateTabs {

    private String tabs;
    private String paths;


    public PrivateTabs(String tabs, String paths) {
        this.tabs = tabs;
        this.paths = paths;
    }


    public String getTabs() {
        return tabs;
    }

    public void setTabs(String tabs) {
        this.tabs = tabs;
    }

    public String getPaths() {
        return paths;
    }

    public void setPaths(String paths) {
        this.paths = paths;
    }



}
